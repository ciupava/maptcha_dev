export function getImages(imageClass: ImageClass, no:number){
  let images=[]
  if (imageClass=== ImageClass.TP){
    images = shuffle(TP)
  }

  if (imageClass=== ImageClass.FP){
    images = shuffle(FP)
    
  }
  
  if (imageClass=== ImageClass.TN){
    
    images = shuffle(TN)
  }

  if (imageClass=== ImageClass.FN){
    images = shuffle(FN)
    
  }

  return images.slice(0,no).map((img:String)=> `https://allofthedata.s3.us-west-2.amazonaws.com/maptcha/data/${imageClass}/${img}`)
  
}

